// Helper function to get full Cloudinary URL
export function getCloudinaryUrl(path: string, transforms?: string): string {
  if (transforms) {
    return `${CLOUDINARY_BASE_URL}/${transforms}/${path}`;
  }
  return `${CLOUDINARY_BASE_URL}/${path}`;
}
